#include <stdlib.h>

int	*ft_range(int min, int max)
{
	int	*arr;
	int	i;

	if (min >= max)
		return (NULL);
	arr = (int *)malloc(sizeof(int) * (max - min));
	i = min;
	while (i < max)
	{
		arr[i - min] = i;
		i++;
	}
	return (arr);
}
