#include<unistd.h>

void	ft_print_comb2(void)
{
	char	*nbrs;
	int		i;
	int		j;

	nbrs = "0123456789";
	i = -1;
	while (++i <= 98)
	{
		j = i;
		while (++j <= 99)
		{
			write(1, &nbrs[i / 10], 1);
			write(1, &nbrs[i % 10], 1);
			write(1, " ", 1);
			write(1, &nbrs[j / 10], 1);
			write(1, &nbrs[j % 10], 1);
			if (i != 98 || j != 99)
				write(1, ",  ", 2);
		}
	}	
}
