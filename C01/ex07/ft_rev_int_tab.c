void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	temp;
	int	k;

	i = 0;
	k = size - 1;
	while (i < (size / 2))
	{
		temp = tab[i];
		tab[i] = tab[k];
		tab[k] = temp;
		i++;
		k--;
	}
}
